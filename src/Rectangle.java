public class Rectangle {
    /**
     * x coordinate.
     */
    private double x;

    /**
     * y coordinate.
     */
    private double y;

    /**
     * Width of rectangle.
     */
    private double width;

    /**
     * Height of rectangle.
     */
    private double height;

    /**
     * Creates an instance of Rectangle with default values.
     */
    public Rectangle() {
        this.x = 0;
        this.y = 0;
        this.width = 0;
        this.height = 0;
    }
}
